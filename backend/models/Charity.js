import mongoose from "mongoose";

const charitySchema = new mongoose.Schema({
    title: {
        type: String,
        required: true
    },
    description: {
        type: String,
        required: true
    },
    date: {
        type: Date,
        required: true
    },
    location: {
        type: String,
        required: true
    },
});

const Charity = mongoose.model('Charity', charitySchema);
export default Charity;